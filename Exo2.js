const calculator = {
    add: (a, b) => {
        return a+b
    },
    subtract: (a, b) => {
        return a-b
    },
    multiply: (a, b) => {
        return a*b
    },
    divide: (a, b) => {
        return a/b
    },  
};

console.log(calculator.add(1, 1));
console.log(calculator.divide(10, 2));
